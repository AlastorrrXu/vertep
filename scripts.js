let currentLanguage = "en";

// 渲染剧本
function renderScript() {
const container = document.getElementById("script-content");
container.innerHTML = "";
scriptData[currentLanguage].forEach((block) => {
    const scriptBlock = document.createElement("div");
    scriptBlock.classList.add("script-block");

    const character = document.createElement("h3");
    character.textContent = block.character;

    const text = document.createElement("p");
    text.textContent = block.text;

    scriptBlock.appendChild(character);
    scriptBlock.appendChild(text);
    container.appendChild(scriptBlock);
});
}

// 切换语言
document.getElementById("toggle-language").addEventListener("click", () => {
currentLanguage = currentLanguage === "en" ? "ua" : "en";
document.getElementById("toggle-language").textContent = currentLanguage === "en" ? "Switch to Ukrainian" : "Switch to English";
renderScript();
});

// 初始化渲染
renderScript();
